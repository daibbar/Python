def ft_harvest_total():
    harvest_weight_1 = int(input("Day 1 harvest: "))
    harvest_weight_2 = int(input("Day 2 harvest: "))
    harvest_weight_3 = int(input("Day 3 harvest: "))
    total_harvest = harvest_weight_1 + harvest_weight_2 + harvest_weight_3
